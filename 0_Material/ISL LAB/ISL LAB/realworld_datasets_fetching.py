import matplotlib.pyplot as plt
import numpy
import pandas as pd
from sklearn.datasets import fetch_20newsgroups
from sklearn.datasets import fetch_covtype
from sklearn.feature_extraction.text import TfidfVectorizer

newsgroups = fetch_20newsgroups(subset='all', remove=('headers', 'footers', 'quotes'))

print("Number of documents in newsgroups dataset:", len(newsgroups.data))
print("Number of categories:", len(newsgroups.target_names))

# Display the first few documents in the dataset
print("\nFirst 2 documents in the newsgroups dataset:")
for i in range(2):
    print("\nCategory:", newsgroups.target_names[newsgroups.target[i]])
    print("Document:\n", newsgroups.data[i])

forest_cover = fetch_covtype()

forest_df = pd.DataFrame(forest_cover.data, columns=[f'feature_{i}' for i in range(forest_cover.data.shape[1])])
forest_df['cover_type'] = forest_cover.target
print("Cover Types:", forest_cover.target_names)
print("Number of samples:", len(forest_df))

# Let's display a bar plot of the distribution of cover types
plt.figure(figsize=(10, 6))
forest_df['cover_type'].value_counts().sort_index().plot(kind='bar', color='forestgreen')
plt.xlabel("Cover Type")
plt.ylabel("Number of Samples")
plt.title("Distribution of Cover Types in Forest Cover Types Dataset")
plt.xticks(rotation=0)
plt.show()